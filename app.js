const calculator = {
    add : function (a, b, c) {
        console.log(a + b + c);
    },
    min : function (a, b, c) {
        console.log(a - b - c);
    },
    div : function (a, b, c) {
        console.log(a / b / c);
    },
    squ : function (a, b, c) {
        console.log(a ** b ** c);
    },
    mul : function (a, b, c) {
        console.log(a * b * c);
    },
};
calculator.add(7, 7, 7);
calculator.min(7, 7, 7);
calculator.div(7, 7, 7);
calculator.squ(7, 7, 7);
calculator.mul(7, 7, 7);